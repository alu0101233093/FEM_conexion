public class MainActivity extends AppCompatActivity {

    private static final String TAG = "MainActivity";
    private FirebaseAuth mAuth;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);
        mAuth = FirebaseAuth.getInstance();

        findViewById(R.id.Search).setOnClickListener(view -> startActivity(new Intent(this, SearchAirportsActivity.class)));
        findViewById(R.id.MyAirports).setOnClickListener(view -> startActivity(new Intent(this, MyAirportsActivity.class)));
        checkLogged();
    }

    @Override
    protected void onResume() {
        super.onResume();
        checkLogged();
    }

    private void checkLogged(){
        if(mAuth.getCurrentUser() == null){
            startActivity(new Intent(this, EmailPasswordActivity.class));
        }
    }

    public void logOut(View v){
        mAuth.signOut();
        checkLogged();
    }

}